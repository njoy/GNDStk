   ext(hdr,src,"void");
   two(hdr,src,"@Delete(", per.clname, false);
   two(hdr,src,"ConstHandle2Const@ self", per.clname, false);
   sig(hdr,src);
   src(1,"detail::deleteHandle<CPP,C>");
